/*
**1. 避免 “重复定义” 编译错误
	头文件（.h）的作用是 “被其他源文件（.c）#include包含”
	如果把函数 ** 定义（功能代码）** 写进头文件，当多个.c 文件包含这个头文件时：
	每个.c 文件编译时都会生成一份该函数的二进制代码，
最终链接（把多个目标文件合并成可执行文件）时，链接器会发现 “同一个函数存在多份实现”，直接报multiple definition（重复定义）错误。
**2. 实现 “接口与实现分离”
	头文件只写函数声明（暴露 “怎么用”），源文件写函数定义（隐藏 “怎么实现”），是模块化开发的核心思路：
	调用者只需要看头文件的声明（知道函数名、参数、返回值），不用关心内部代码逻辑；
	后续修改函数的实现细节（比如把scanf改成更安全的输入方式），只需要改源文件，不用动头文件 —— 调用者的代码不需要重新编译，降低维护成本。
**3. 提升编译效率
	如果函数定义写进头文件，每次头文件被包含时，编译器都要重新编译一遍函数代码；
	而把定义放在源文件里，源文件只需要编译一次（生成目标文件.o），后续其他文件包含头文件时，只需要用声明 “占位”，不用重复编译实现代码，大幅提升大型项目的编译速度。
	简单说：头文件是 “对外的接口说明书”，源文件是 “接口的具体实现车间”—— 分开写是 C/C++ 工程化开发的 “约定俗成”，既避免编译错误，也让代码更易维护。
*/
